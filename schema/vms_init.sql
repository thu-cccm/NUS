-- 新农村建设信息管理系统初始化脚本
DROP TABLE IF EXISTS `vms_resident`;

CREATE TABLE IF NOT EXISTS `vms_resident` (
  `id` bigint(20) NOT NULL AUTO_INCREMENT COMMENT '主键',
  `real_name` varchar(20) NOT NULL COMMENT '真实姓名',
  `id_card` varchar(18) NOT NULL COMMENT '身份证号',
  `gender` tinyint(4) DEFAULT NULL COMMENT '性别(0女,1男)',
  `age` int(11) DEFAULT NULL COMMENT '年龄',
  `phone` varchar(15) DEFAULT NULL COMMENT '联系电话',
  `group_no` int(11) DEFAULT NULL COMMENT '所属村组',
  `politics` varchar(10) DEFAULT NULL COMMENT '政治面貌',
  `is_poor` tinyint(4) DEFAULT 0 COMMENT '是否贫困户(0否,1是)',
  `health_status` varchar(20) DEFAULT NULL COMMENT '健康状况',
  `create_time` datetime DEFAULT NULL COMMENT '创建时间',
  PRIMARY KEY (`id`),
  UNIQUE KEY `uk_resident_id_card` (`id_card`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='农村人口档案表';

DROP TABLE IF EXISTS `vms_land`;

CREATE TABLE IF NOT EXISTS `vms_land` (
  `id` bigint(20) NOT NULL AUTO_INCREMENT COMMENT '主键',
  `resident_id` bigint(20) DEFAULT NULL COMMENT '关联户主',
  `land_code` varchar(30) DEFAULT NULL COMMENT '地块编号',
  `area` decimal(10,2) DEFAULT NULL COMMENT '面积(亩)',
  `type` varchar(10) DEFAULT NULL COMMENT '土地类型',
  `location` varchar(100) DEFAULT NULL COMMENT '地理位置描述',
  `status` tinyint(4) DEFAULT 0 COMMENT '确权状态(0待确权,1已确权)',
  `cert_url` varchar(255) DEFAULT NULL COMMENT '确权证书图片路径',
  PRIMARY KEY (`id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='土地资源管理表';

DROP TABLE IF EXISTS `vms_agriculture`;

CREATE TABLE IF NOT EXISTS `vms_agriculture` (
  `id` bigint(20) NOT NULL AUTO_INCREMENT COMMENT '主键',
  `resident_id` bigint(20) DEFAULT NULL COMMENT '种植负责人',
  `season` varchar(20) DEFAULT NULL COMMENT '种植季度',
  `crop_name` varchar(20) DEFAULT NULL COMMENT '作物名称',
  `plant_area` decimal(10,2) DEFAULT NULL COMMENT '种植面积',
  `expect_yield` decimal(10,2) DEFAULT NULL COMMENT '预计产量(公斤)',
  `market_price` decimal(10,2) DEFAULT NULL COMMENT '预估单价',
  PRIMARY KEY (`id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='农业生产管理表';

DROP TABLE IF EXISTS `vms_infrastructure`;

CREATE TABLE IF NOT EXISTS `vms_infrastructure` (
  `id` bigint(20) NOT NULL AUTO_INCREMENT COMMENT '主键',
  `project_name` varchar(50) DEFAULT NULL COMMENT '项目名称',
  `budget` decimal(12,2) DEFAULT NULL COMMENT '项目预算',
  `start_date` date DEFAULT NULL COMMENT '开工日期',
  `contractor` varchar(50) DEFAULT NULL COMMENT '施工单位',
  `progress` int(11) DEFAULT NULL COMMENT '工程进度(0-100)',
  `status` varchar(10) DEFAULT NULL COMMENT '状态',
  PRIMARY KEY (`id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='基础设施建设表';

DROP TABLE IF EXISTS `vms_apply`;

CREATE TABLE IF NOT EXISTS `vms_apply` (
  `id` bigint(20) NOT NULL AUTO_INCREMENT COMMENT '主键',
  `resident_id` bigint(20) DEFAULT NULL COMMENT '申请人ID',
  `apply_type` varchar(20) DEFAULT NULL COMMENT '申请类型',
  `content` text COMMENT '申请详细描述',
  `proof_files` text COMMENT '佐证材料图片链接',
  `status` tinyint(4) DEFAULT 0 COMMENT '状态(0待审核,1已通过,2已驳回)',
  `audit_by` varchar(20) DEFAULT NULL COMMENT '审核人姓名',
  `audit_reply` varchar(255) DEFAULT NULL COMMENT '审核意见/驳回理由',
  `create_time` datetime DEFAULT NULL COMMENT '创建时间',
  `audit_time` datetime DEFAULT NULL COMMENT '审核时间',
  PRIMARY KEY (`id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='事务申请审批表';

DROP TABLE IF EXISTS `vms_vote`;

